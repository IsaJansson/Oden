<h2>Download</h2>
<p>You can download Oden from <a href='https://github.com/IsaJansson/Oden'>GitHub</a>, 
or you can clone it using the link below.</p>
<blockquote>
<code>git clone git://github.com/IsaJansson/Oden.git</code>
</blockquote>


<h2>Installation</h2>
<p>First if you haven't already made the data-directory and the grid-directory writable you have to do that now. 
The data-directory is the place where Oden needs
to be able to write and create files.</p>
<blockquote>
<code>cd Oden; chmod 777 site/data</code> <br />
<code>cd Oden; chmod 777 theme/grid</code> <br />
</blockquote>

<p>If the first step is done Oden has some modules that need to be initialised before you can build your site. 
This will be done by clicking on the link below.</p>
<blockquote>
<a href='<?=create_url('module/install')?>'>Install Oden</a>
</blockquote>

